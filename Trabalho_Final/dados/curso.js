class curso{
    constructor(nome, nro_departamento){
        this.cod_curso = cod_curso;
        this.nome = nome;
        this.nro_departamento = nro_departamento;
    }

    getCodCurso(){
        return this.cod_curso;
    }

    getNome(){
        return this.nome;
    }
    getNro(){
        return this.nro_departamento;
    }
}